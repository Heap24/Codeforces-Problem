#include <bits/stdc++.h>
using namespace std;

int main() {
    // Initialize and assign values
    int a = 10;  // assigning 10 to variable 'a'
    int b = 20;  // assigning 20 to variable 'b'

    // Perform addition
    int c = a + b;  // adding 'a' and 'b' and storing the result in 'c'

    // Output the result
    cout << "The sum of a and b is: " << c << endl;
    
    return 0;
}
