#include <bits/stdc++.h>
using namespace std;

int main(){
    int a;
    char b;
    float c;
    double d;
    bool e;
    long int i;
    short int j;
    long long int k;
    cout<<"size of int"<<sizeof(a)<<endl;
    cout<<"size of char"<<sizeof(b)<<endl;
    cout<<"size of float"<<sizeof(c)<<endl;
    cout<<"size of double"<<sizeof(d)<<endl;
    cout<<"size of bool"<<sizeof(e)<<endl;
    cout<<"size of long int"<<sizeof(i)<<endl;
    cout<<"size of short int"<<sizeof(j)<<endl;
    cout<<"size of long long int"<<sizeof(k)<<endl;
    return 0;
}
