#include <bits/stdc++.h>
using namespace std;

int main() {
    // Declare variables
    int a, b;

    // Take input from the user
    cout << "Enter two integers: ";
    cin >> a >> b;

    // Perform and display various arithmetic operations
    cout << "Sum (a + b)       : " << (a + b) << endl;  // addition
    cout << "Difference (a - b): " << (a - b) << endl;  // subtraction
    cout << "Product (a * b)   : " << (a * b) << endl;  // multiplication
    cout << "Quotient (a / b)  : " << (a / b) << endl;  // division
    cout << "Remainder (a % b) : " << (a % b) << endl;  // modulus

    return 0;
}
